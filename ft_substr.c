#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	j = 0;
	if (start >= ft_strlen(s))
		str = malloc(sizeof(char));
	else if (len >= ft_strlen(s))
		str = malloc(ft_strlen(s) - start + 1);
	else
		str = malloc(len + 1);
	if (str == 0)
		return (0);
	while (s[i] != '\0')
	{
		if (i >= start && j < len)
		{
			str[j] = s[i];
			j++;
		}
		i++;
	}
	str[j] = '\0';
	return (str);
}
