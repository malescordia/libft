#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*b;

	i = 0;
	b = s;
	while ((size_t)i < n)
	{
		b[i] = c;
		i++;
	}
	return (s);
}
